    <h1>WORKPLACE POLICIES</h1>
      <p class="w3-xlarge w3-text-dark-grey"></p>

<p>
<strong>1.</strong> At-will employment. This statement reiterates that either you or the employee can terminate the employment relationship at any time and for any reason, as long as the reason is a lawful one. It is a best practice to prominently display this statement in the beginning of your employee handbook (except in Montana, where at-will employment is not recognized). Reinforce at-will status in your handbook acknowledgment form as well.
</p>
<p>
<strong>2.</strong> Anti-harassment and non-discrimination. These policies prohibit harassment and discrimination in the workplace. Non-discrimination laws are governed by federal, state and local provisions, so review your applicable law and account for all appropriate protections.
</p>
<p>
<strong>3.</strong> Employment classifications. It is a best practice to clearly define employment classifications, such as full-time, part-time, exempt or non-exempt since an employee's classification can dictate eligibility for benefits and overtime pay.
</p>
<p>
<strong>4.</strong> Leave and time off benefits. These policies address a company's rules and procedures regarding holidays, vacation, sick, and other types of time off benefits, or leave required by law (such as voting leave, family leave, and domestic violence leave) or company policy. Check your state and local law to ensure all leave requirements are included in your employee handbook.
</p>
<p>
<strong>5.</strong> Meal and break periods. A policy on meal and break periods informs employees of the frequency and duration of such breaks as well as any rules or restrictions related to break periods. Rest periods, lactation breaks, and meal periods must be provided in accordance with federal, state and local laws.
</p>
<p>
<strong>6.</strong> Timekeeping and pay. A timekeeping policy informs employees of the method for recording time worked and the importance of accurately recording their time. A policy on paydays lets employees know the frequency of paydays, the methods available for receiving pay, and any special procedures for when a payday falls on a holiday or when an employee is absent from work. 
</p>
<p>
<strong>7.</strong> Safety and health. Safety policies describe safety and emergency procedures and require employees to report work-related injuries immediately. Additionally, some regulations under the Occupational Safety and Health Act require employers to have specific policies and programs in place if certain workplace hazards exist (such as a hazard communication program if certain chemicals are present in the workplace).
</p>
<p>
<strong>8.</strong> Employee conduct, attendance and punctuality. Attendance policies make it clear that employees must be ready to work at their scheduled start time each day and provide procedures for informing the company of an unscheduled absence or late arrival. It is also a best practice to have policies on standards of conduct, drug and alcohol abuse, disciplinary action, confidentiality, conflicts of interest, and workplace violence.
</p>


